package struct;

public class PrimaryKey {
    public PrimaryKey() {
    }

    public PrimaryKey(String name) {
        this.name = name;
    }

    private String name;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }
}
